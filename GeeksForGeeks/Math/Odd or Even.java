class Solution {
    static boolean isEven(int n) {
        if(n % 2 == 0){
            return true;
        }else{
            return false;
        }
    }
}

/*Approach

Check whether the given number is even by using the modulo operator.
If n % 2 equals 0, the number is even.

Time Complexity:
O(1)

Space Complexity:
O(1)
*/
